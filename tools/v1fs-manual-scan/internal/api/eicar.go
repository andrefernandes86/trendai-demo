package api

// eicarProbe returns the 68-byte EICAR test-file content.
//
// The bytes are stored XOR-encoded (key 0x5A) so no recognisable AV signature
// exists in the compiled binary. They are decoded only at the moment of use,
// immediately before the buffer is handed to the scanner — never written to
// disk except inside the temporary scan directory created by startTestScan.
func eicarProbe() []byte {
	// Each byte = original EICAR byte ^ 0x5A
	encoded := []byte{
		0x02, 0x6F, 0x15, 0x7B, 0x0A, 0x7F, 0x1A, 0x1B,
		0x0A, 0x01, 0x6E, 0x06, 0x0A, 0x00, 0x02, 0x6F,
		0x6E, 0x72, 0x0A, 0x04, 0x73, 0x6D, 0x19, 0x19,
		0x73, 0x6D, 0x27, 0x7E, 0x1F, 0x13, 0x19, 0x1B,
		0x08, 0x77, 0x09, 0x0E, 0x1B, 0x14, 0x1E, 0x1B,
		0x08, 0x1E, 0x77, 0x1B, 0x14, 0x0E, 0x13, 0x0C,
		0x13, 0x08, 0x0F, 0x09, 0x77, 0x0E, 0x1F, 0x09,
		0x0E, 0x77, 0x1C, 0x13, 0x16, 0x1F, 0x7B, 0x7E,
		0x12, 0x71, 0x12, 0x70,
	}
	out := make([]byte, len(encoded))
	for i, b := range encoded {
		out[i] = b ^ 0x5A
	}
	return out
}
